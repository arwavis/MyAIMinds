#Operators in R

#Arithmatic operators +,-,*,/,%%, %/%, ^ 
a<-10
b<-5

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%%b) #remainder 
print(a%/%b) #quotient 
print(a^b) #power

#Relational operators >, <, >=, <=,  !=, Output will always be a Logical datatype

a<-10
b<-5

print(a>b)
print(a<b)
print(a>=b)
print(a<=b)
print(a!=b) 

